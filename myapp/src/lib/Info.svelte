  <div class="text-white text-7xl text-center p-5">La tecnica del pomodoro</div>
  <div>
    The Pomodoro Technique, è una soluzione molto efficace per gestire il tempo.
    Inventata negli anni ’80 da Francesco Cirillo, uno sviluppatore di software,
    deve il suo nome ad un timer a forma di pomodoro utilizzato dal creatore, e
    sul quale si basa l’intera tecnica. In pratica, la Tecnica del Pomodoro si
    basa su un concetto molto semplice: durante i minuti dedicati al lavoro,
    scanditi dal timer, devi fare esclusivamente quell’operazione, senza
    distrarti per guardare il cellulare oppure controllare le e-mail. Devi solo
    studiare, o lavorare. Lo schema della Tecnica del Pomodoro è molto semplice
    ed immediato: Stabilisci i compiti da svolgere durante la giornata; Lavora
    per 25 minuti senza distrazioni; Quando il timer segna la fine dei 25 minuti
    fermati; Fai una pausa di 5 minuti; Ricomincia a lavorare per altri 25
    minuti. Ogni 4 sessioni (ovvero ogni due ore) prenditi una pausa di 15/30
    minuti.
